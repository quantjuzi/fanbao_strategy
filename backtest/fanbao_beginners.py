# 你想改什么条件，就在下面 "选股条件" 那个区域改。
# 每行条件后面都有注释，告诉你它对应什么含义。
#
# 目前已经测过的因子：
#
# 1. 成交额门槛（基础值15亿）
#                   15亿       20亿       25亿
#   信号总笔数       629        490        373
#   胜率          50.9%      51.2%      52.5%
#   平均盈亏      +1.07%     +1.28%     +1.41%
#   → 25亿最优，但信号偏少
#
# 2. 加上 T-3 也涨停（连2板）
#                   基础版    +T-3涨停
#   信号总笔数       629        146
#   胜率          50.9%      51.4%
#   平均盈亏      +1.07%     +1.07%
#   → 信号少了77%，日常操作不采用
#
# 4. M7/M14 比值调整
#   M7/M14         信号    胜率    平均盈亏   平均亏    盈亏比
#   > 1.0          420   51.9%    +1.20%   -5.49%    1.35
#   > 1.05         231   52.8%    +1.39%   -5.74%    1.35  ← 最优
#   > 1.07         160   52.5%    +1.20%   -6.42%    1.26
#   > 1.1           85   50.6%    +0.30%   -7.65%    1.05
#
#   为什么 1.05 最优？
#   比值调到 1.05，把弱势票筛掉了，胜率和收益都提高。
#   但超过 1.05 之后，平均亏损大幅增加（1.07亏-6.42%、1.1亏-7.65%），
#   因为留下的票波动更大，单笔亏损更严重。
#   1.05 是刚好把差的筛掉、又没让风险失控的平衡点。
#
# ============================================================
#                   基础版    +实体>-3%
#   信号总笔数       629        420
#   胜率          50.9%      51.9%
#   平均盈亏      +1.07%     +1.20%
#   → 胜率和收益都提高了，采用
#
# ============================================================

import pandas as pd, numpy as np
import sys, io
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

CSV = r"C:\Users\Administrator\Desktop\全市场A股_2026-04-12_2026-06-12.csv"
df = pd.read_csv(CSV)
df["index"] = pd.to_datetime(df["index"])
df.sort_values(["证券代码", "index"], inplace=True)

df["M7"] = df.groupby("证券代码")["close"].transform(lambda x: x.rolling(7).mean())
df["M14"] = df.groupby("证券代码")["close"].transform(lambda x: x.rolling(14).mean())
df["zt_Tm2"] = df.groupby("证券代码")["是否涨停"].shift(1)

# ======================= 选股条件 =======================

df["实体涨跌幅"] = (df["close"] - df["open"]) / df["open"]   # 算实体涨跌幅

df["信号"] = (
    (df["zt_Tm2"] == 1) &              # T-2涨停过
    (df["是否涨停"] == 0) &             # T-1没涨停（断板）
    (df["money"] > 1_500_000_000) &     # T-1成交额>15亿（改这个数字改门槛）
    (df["M7"] / df["M14"] > 1) &        # 👈 改这个数字调M7/M14比值（1.0/1.05/1.07/1.1）
    (df["实体涨跌幅"] > -0.03) &         # 实体涨跌幅>-3%（删掉这行就去掉了）
    df["M14"].notna()
).astype(int)

# ======================= 下面不用改 =======================

print(f"信号总数: {df['信号'].sum()}")
print()

trades = []
for _, row in df[df["信号"] == 1].iterrows():
    code = row["证券代码"]; t1 = row["index"]
    td = df[(df["证券代码"] == code) & (df["index"] > t1)]
    if td.empty: continue
    bp = td.iloc[0]["open"]
    tp1 = df[(df["证券代码"] == code) & (df["index"] > td.iloc[0]["index"])]
    if tp1.empty: continue
    sp = tp1.iloc[0]["close"]; pnl = (sp - bp) / bp * 100
    trades.append({"code": code, "bp": bp, "sp": sp, "pnl": pnl})

pnls = [t["pnl"] for t in trades]
w_pnls = [p for p in pnls if p > 0]
l_pnls = [p for p in pnls if p <= 0]

print(f"信号总笔数    {len(pnls)}")
print(f"盈利笔数      {len(w_pnls)}")
print(f"亏损笔数      {len(l_pnls)}")
print(f"胜率          {len(w_pnls)}/{len(pnls)} ({len(w_pnls)/len(pnls)*100:.1f}%)")
print(f"平均盈亏      {np.mean(pnls):+.2f}%")
print(f"平均盈利      {np.mean(w_pnls):+.2f}%")
print(f"平均亏损      {np.mean(l_pnls):+.2f}%")
print(f"盈亏比        {abs(np.mean(w_pnls)/np.mean(l_pnls)):.2f}")
print(f"最大盈利      {max(pnls):+.2f}%")
print(f"最大亏损      {min(pnls):+.2f}%")
print(f"收益标准差    {np.std(pnls):.2f}%")
print()
print()
